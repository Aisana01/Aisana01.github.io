$(document).ready(function () {
    var offset = $('#m_block1').offset();
     var topPadding = 0;
     $(window).scroll(function() {
         if ($(window).scrollTop() > offset.top) {
             $('#m_block1').stop().animate({marginTop: $(window).scrollTop() - offset.top + topPadding});
         }
         else {
             $('#m_block1').stop().animate({marginTop: 0});
         }
     });
 });
